def soccer_clubs (love, hate):
    print(f"I absolutely love {love}!")
    print(f"But I despise {hate}!")
    print(" \n")

soccer_clubs('Man United', 'Liverpool')

soccer_clubs(input("What club do you hate?: "), input("What club do you love?: "))

fav = 'Barcelona'
least = 'Real Madrid'
soccer_clubs(fav, least)
