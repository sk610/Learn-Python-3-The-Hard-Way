# + plus - adds numbers together
# - minus
# / slash - indicates division
# * asterisk
# % percent - modulo = a / b with remainder c
# < less than
# > greater than
# <= less than equal
# >= greater than equal

# prints the string
print("I will now count my chickens:")

# divides 30 by 6 and adds 25
print("Hens", 25 + 30 / 6)

# multiplies 25 x 3, then 74 modulo 4, with a remainder of 3, then deducts 3 from 100
print("Roosters", 100 - 25 * 3 % 4)

# prints the string
print("Now I will count the eggs:")

# starting with division, then modulo, then addition and substraction
print(3.0 + 2.0 + 1.0 - 5.0 + 4.0 % 2.0 - 1.0/4.0 + 6.0)

# prints the string
print("Is it true that 3 + 2 < 5 - 7?")

# solves the problem
print(3 + 2 < 5 - 7)

# prints the string and provides the answer outside of the sting
print("What is 3 + 2?", 3 + 2)
print("What is 5 - 7?", 5 - 7)

# prints the string
print("Oh, that's why it's False.")

# prints the string
print("How about some more.")

# prints the string and gives answer for the problem outside of the sting
print("Is it great?", 5 > -2)
print("Is it greater or equal?", 5 >= -2)
print("Is it less or equal?", 5 <= -2)
